/** Generar todas las combinaciones para generateStaticParams */
export function getAllServiceModifierParams() {
  const params = [];

  for (const serviceSlug of getServiceSlugs()) {
    for (const citySlug of getCitySlugs()) {
      params.push({ service: serviceSlug, modifier: citySlug });
    }
    for (const sectorSlug of getSectorSlugs()) {
      params.push({ service: serviceSlug, modifier: sectorSlug });
    }
  }

  return params;
}

